public class Point {
    private float x;
    private float Y;

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return Y;
    }

    public void setY(float y) {
        Y = y;
    }

    public Point(float x, float y) {
        this.x = x;
        Y = y;
    }
}
